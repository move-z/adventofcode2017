import sys
from collections import defaultdict

pid = 0
series = [1]


class Coordinate:
    def __init__(self, x, y, z):
        self.x = x
        self.y = y
        self.z = z

    def distance(self):
        return abs(self.x) + abs(self.y) + abs(self.z)

    def __add__(self, c):
        return Coordinate(self.x + c.x, self.y + c.y, self.z + c.z)

    def __eq__(self, c):
        return self.x == c.x and self.y == c.y and self.z == c.z

    def __lt__(self, c):
        return self.distance() < c.distance()

    def __le__(self, c):
        return self.distance() <= c.distance()

    def __gt__(self, c):
        return self.distance() > c.distance()

    def __ge__(self, c):
        return self.distance() >= c.distance()


class Particle:
    def __init__(self, position, velocity, acceleration):
        global pid
        self.position = position
        self.velocity = velocity
        self.acceleration = acceleration
        self.pid = pid
        pid += 1

    def advance(self):
        self.velocity += self.acceleration
        self.position += self.velocity

    def position_at(self, time):
        x = self.__position(self.position.x, self.velocity.x, self.acceleration.x, time)
        y = self.__position(self.position.y, self.velocity.y, self.acceleration.y, time)
        z = self.__position(self.position.z, self.velocity.z, self.acceleration.z, time)
        return Coordinate(x, y, z)

    def intersects(self, p):
        x = Particle.__intersects(self.position.x, self.velocity.x, self.acceleration.x,
                                  p.position.x, p.velocity.x, p.acceleration.x)
        y = Particle.__intersects(self.position.y, self.velocity.y, self.acceleration.y,
                                  p.position.y, p.velocity.y, p.acceleration.y)
        z = Particle.__intersects(self.position.z, self.velocity.z, self.acceleration.z,
                                  p.position.z, p.velocity.z, p.acceleration.z)

        # per almeno 1 delle coordinate non ci sono scontri
        if x is None or y is None or z is None:
            return None

        # le particelle si incontrano se le coordinate di incontrano nello stesso momento
        intersection = set(x).intersection(set(y)).intersection(set(z))
        if intersection:
            return min(intersection)
        else:
            return None

    @staticmethod
    def __position(p, v, a, time):
        return p + v * time + a * Particle.__accel_factor(time)

    @staticmethod
    def __intersects(p1, v1, a1, p2, v2, a2):
        # dalla formula della posizione una coordinata si interseca se il delta Ã¨ 0
        p = p1 - p2
        v = v1 - v2
        a = a1 - a2

        # casi limite
        if a == 0:
            if v == 0:
                return (0,) if p == 0 else None
            else:
                t = -1 * p / v
                return (int(t),) if t == int(t) else None

        prev_distance = None
        zeroes = []
        for t in range(sys.maxsize):
            distance = p + v * t + a * Particle.__accel_factor(t)
            if distance == 0:
                zeroes.append(t)

            # se stiamo divergendo non continuo
            if t == 0:
                prev_distance = distance
                continue
            if abs(v + a * t) > abs(v + a * (t - 1)) and abs(distance) > abs(prev_distance):
                break
            prev_distance = distance

        return zeroes

    # l'accelerazione al tempo t concorre per un fattore pari a t + f(t-1)
    # viste i numeri uso una cache dei risultati calcolati
    @staticmethod
    def __accel_factor(time):
        global series

        if time == 0:
            return 0

        while len(series) < time:
            last = series[-1]
            found = len(series) + 1 + last
            series.append(found)
        return series[time - 1]


def first(*particles):
    return particles.index(min(particles, key=lambda p: p.position_at(5_000_000)))


def second(*particles):
    remaining = list(particles)

    intersections = defaultdict(lambda: set())
    for i in range(len(remaining)):
        for j in range(i + 1, len(remaining)):
            a = remaining[i]
            b = remaining[j]
            collision = a.intersects(b)
            if collision is not None:
                intersections[collision].update((a, b))

    while True:
        if not intersections:
            break
        intervals = intersections.keys()
        first_collision = min(intervals)
        collisions = intersections[first_collision]
        for particle in collisions:
            del remaining[remaining.index(particle)]
            for t in intervals:
                if t == first_collision:
                    continue
                c = intersections[t]
                if particle in c:
                    c.remove(particle)
                    if len(c) <= 1:
                        del intersections[t]
        del intersections[first_collision]

    return len(remaining)


