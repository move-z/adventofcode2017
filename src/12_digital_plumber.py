class Program:
    def __init__(self, pid, siblings):
        self.pid = pid
        self.siblings = siblings


def first(*programs):
    directory = {p.pid: p for p in programs}
    bag = set()

    def populate(p):
        if p.pid not in bag:
            bag.add(p.pid)

        for s in p.siblings:
            if s not in bag:
                populate(directory[s])

    populate(programs[0])

    return len(bag)


def second(*programs):
    remaining = set(programs[:])
    directory = {p.pid: p for p in programs}

    def populate(p, bag):
        if p in remaining:
            remaining.discard(p)

        if p.pid not in bag:
            bag.add(p.pid)

        for s in p.siblings:
            if s not in bag:
                populate(directory[s], bag)

    total = 0
    while len(remaining) > 0:
        total += 1
        populate(remaining.pop(), set())

    return total


